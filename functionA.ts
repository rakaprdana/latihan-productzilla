export function sum(a: number, b: number) {
  return a + b;
}

export const subtract = (a: number, b: number) => {
  return a - b;
};

export function multiply(a: number, b: number) {
  return a * b;
}

export const divideBy = (a: number, b: number) => {
  return a / b;
};

export default (name: string) => {
  return name;
};
